char	*ft_strrchr(const char *s, int c)
{
	char *res;

	res = 0;
	while (*s != '\0')
	{
		if (*s == c)
			res = (char *)s;
		s++;
	}
	if (c == '\0')
	{
		res = (char *)s;
		return (res);
	}
	return (res);
}

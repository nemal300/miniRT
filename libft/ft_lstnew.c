#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	if (!(new = (t_list *)malloc(sizeof(t_list))))
		return (NULL);
	if (content != NULL)
		new->content = content;
	else
		new->content = NULL;
	new->next = NULL;
	return (new);
}

#include "minirt.h"

int	error_handler(char *s)
{
	int i;

	i = 0;
	write(1, "Error\n", 6);
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
